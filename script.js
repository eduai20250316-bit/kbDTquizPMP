// PWA 설치 프롬프트
let deferredPrompt;
window.addEventListener('beforeinstallprompt', (e) => {
    e.preventDefault();
    deferredPrompt = e;
    
    // 설치 배너 표시 (선택사항)
    setTimeout(() => {
        if (confirm('📱 이 퀴즈 앱을 홈 화면에 추가하시겠습니까?\n\n오프라인에서도 사용할 수 있고 앱처럼 빠르게 접근할 수 있습니다.')) {
            deferredPrompt.prompt();
            deferredPrompt.userChoice.then((choiceResult) => {
                if (choiceResult.outcome === 'accepted') {
                    console.log('사용자가 PWA 설치를 수락했습니다');
                }
                deferredPrompt = null;
            });
        }
    }, 5000); // 5초 후 프롬프트 표시
});

// 키보드 단축키
document.addEventListener('keydown', (e) => {
    // 숫자 키로 선택지 선택 (1-4)
    if (e.key >= '1' && e.key <= '4') {
        const optionIndex = parseInt(e.key) - 1;
        const options = document.querySelectorAll('.option');
        if (options[optionIndex] && document.getElementById('quiz-screen').classList.contains('active')) {
            options[optionIndex].click();
        }
    }
    
    // Enter 키로 버튼 클릭
    if (e.key === 'Enter') {
        const activeScreen = document.querySelector('.screen.active');
        if (activeScreen) {
            const primaryButton = activeScreen.querySelector('.btn-primary:not(:disabled)');
            if (primaryButton) {
                primaryButton.click();
            }
        }
    }
    
    // ESC 키로 시작 화면으로 돌아가기
    if (e.key === 'Escape') {
        if (confirm('퀴즈를 중단하고 시작 화면으로 돌아가시겠습니까?')) {
            document.querySelector('.quiz-app').showScreen('start-screen');
        }
    }
});
